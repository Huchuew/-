function equipLineSet(prefix: string, weapon: string, armor: string, theme: string): CharEquipNames {
  const tiers = ['견습', '숙련', '정예', '영웅', '전설', '신화', '고대', '종말', '불멸', '절대'];
  return {
    weapon: tiers.map((t, i) => i === 0 ? `견습 ${weapon}` : `${t} ${weapon}`),
    armor: tiers.map((t, i) => i === 0 ? `견습 ${armor}` : `${t} ${armor}`),
    ring: tiers.map(t => `${t} ${theme} 반지`),
    necklace: tiers.map(t => `${t} ${theme} 목걸이`),
    relic: tiers.map(t => `${t} ${theme} 유물`),
  };
}

